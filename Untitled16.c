//primitive data types
#include <stdio.h>

int main() {
    // Declare char variables and assign character values
    char initial = 'B';       // Character representing an initial
    char symbol = '#';        // Special character symbol

    // Print the values of the char variables
    printf("Initial: %c\n", initial); // %c is the format specifier for printing char
    printf("Symbol: %c", symbol);     // %c prints the character value

    return 0;
}

