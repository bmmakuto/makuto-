//In C, the double equal sign == is used to check if two values are equal, 
//while the not equal operator != checks if two values are different.
#include <stdio.h>

int main() {
    int number = 10;

    // Check if number is equal to 10
    if (number == 10) {
        printf("Number is exactly ten.\n");
    }

    // Check if number is not equal to 5
    if (number != 5) {
        printf("Number is not five.\n");
    }

    return 0;
}
