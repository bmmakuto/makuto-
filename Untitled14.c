// we demonstrate how to declare and initialize variables using 
//the short and int data types in C. This allows you to store different ranges of integer values
// depending on the memory and size requirements.
#include <stdio.h>

int main() {
    // Declare and initialize a short variable for year of birth
    short birthYear = 2005;

    // Declare and initialize an int variable for student ID
    int studentID = 102345;

    // Display the values
    printf("Year of Birth: %d\n", birthYear);
    printf("Student ID: %d", studentID);

    return 0;
}

