//The program checks if a specific flag is set to 1 (indicating true). 
//If the flag is 1, it prints "Flag is set!".
// If the flag is not 1, the program does nothing
int main() {
    int flag = 1;  // Flag set to 1 (true)

    // Simple if statement checks if the flag is set to 1
    if (flag) {
        printf("Flag is set!\n");
    }

    // Final message after checking
    printf("Flag check completed.\n");

    return 0;
}

